int		ft_sqrt(int nb)
{
	int		i;

	if (nb > 8)
	{
		i = nb / 3;
		while (i > 1 && !(nb / i == i && nb % i == 0))
			i--;
		if (i > 1)
			return (i);
		else
			return (0);
	}
	else if (nb == 1)
		return (1);
	else if (nb == 4)
		return (2);
	else
		return (0);
}
