int		ft_iterative_factorial(int nb)
{
	int		a;

	a = 1;
	if (nb > 0 && nb < 13)
	{
		while (nb >= 1)
		{
			a = a * nb;
			nb--;
		}
		return (a);
	}
	else if (nb == 0)
		return (1);
	else
		return (0);
}
